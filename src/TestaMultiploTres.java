
public class TestaMultiploTres {
	public static void main(String[] args) {
		for(int multiplo = 1; multiplo < 100; multiplo++) {
			if(multiplo % 3 == 0) {
				System.out.print(multiplo);		
				System.out.print(" ");
			}
		}
	}
}

